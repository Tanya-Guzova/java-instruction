
public class UnaryOperatorApp {

	public static void main(String[] args) {
		int i = 1;
		
		//multiple ways to increment
		System.out.println(i);
		
		i = 1 + 1;
		System.out.println(i);
		
		//for incrementing 1 or more
		i += 1;
		System.out.println(i);
		
		////for incrementing 1 only
		i ++;
		System.out.println(i);

	}

}
